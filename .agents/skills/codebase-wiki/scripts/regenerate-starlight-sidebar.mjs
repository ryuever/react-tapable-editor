#!/usr/bin/env node
/**
 * Scan src/content/docs/{architecture,discussion,issue,reference,roadmap}/*.md for
 * frontmatter `id` + `title`, then rewrite .starlight/sidebar.generated.mjs
 * for Astro Starlight.
 *
 * Usage: node regenerate-starlight-sidebar.mjs --root <project-root>
 */

import fs from "node:fs";
import path from "node:path";

const CATEGORIES = [
  { dir: "architecture", label: "架构分析" },
  { dir: "discussion", label: "技术讨论" },
  { dir: "issue", label: "Issue 记录" },
  { dir: "reference", label: "参考手册" },
  { dir: "roadmap", label: "规划路线" },
];

function parseArgs(argv) {
  const out = { root: process.cwd() };
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a === "--root" && argv[i + 1]) {
      out.root = path.resolve(argv[++i]);
    }
  }
  return out;
}

function readFrontmatter(filePath) {
  const raw = fs.readFileSync(filePath, "utf8");
  const m = raw.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  if (!m) return null;
  const block = m[1];
  const map = {};
  for (const line of block.split(/\r?\n/)) {
    const lm = line.match(/^([\w-]+):\s*(.*)$/);
    if (lm) map[lm[1]] = lm[2].trim();
  }
  return map;
}

function idSortKey(id) {
  const s = String(id || "");
  const m = s.match(/^([ADIRP])-(\d{3})$/);
  if (!m) return [999, s];
  const prefixOrder = { A: 0, D: 1, I: 2, R: 3, P: 4 };
  return [prefixOrder[m[1]] ?? 50, Number(m[2], 10)];
}

function compareById(a, b) {
  const ka = idSortKey(a.id);
  const kb = idSortKey(b.id);
  if (ka[0] !== kb[0]) return ka[0] - kb[0];
  if (typeof ka[1] === "number" && typeof kb[1] === "number")
    return ka[1] - kb[1];
  return String(ka[1]).localeCompare(String(kb[1]));
}

function main() {
  const { root } = parseArgs(process.argv);
  const docsDir = path.join(root, "src", "content", "docs");
  const outDir = path.join(root, ".starlight");
  const outFile = path.join(outDir, "sidebar.generated.mjs");

  if (!fs.existsSync(docsDir)) {
    console.error(`Missing docs directory: ${docsDir}`);
    process.exit(1);
  }

  fs.mkdirSync(outDir, { recursive: true });

  /**
   * Build Starlight sidebar config.
   * @see https://starlight.astro.build/reference/configuration/#sidebar
   */
  const sidebar = [];

  for (const { dir, label } of CATEGORIES) {
    const dirPath = path.join(docsDir, dir);
    const items = [];

    if (fs.existsSync(dirPath)) {
      const names = fs
        .readdirSync(dirPath)
        .filter((n) => n.endsWith(".md") || n.endsWith(".mdx"))
        .sort();

      const metas = [];
      for (const name of names) {
        const fp = path.join(dirPath, name);
        const fm = readFrontmatter(fp);
        if (!fm || !fm.id) continue;
        const base = name.replace(/\.mdx?$/i, "");
        const slug = `${dir}/${base}`;
        const title = fm.title || fm.id;
        metas.push({ id: fm.id, slug, label: `${fm.id} ${title}` });
      }
      metas.sort(compareById);
      for (const row of metas) {
        items.push({ label: row.label, slug: row.slug });
      }
    }

    if (items.length > 0) {
      sidebar.push({ label, items });
    } else {
      // Even empty groups — use autogenerate so new docs show up automatically
      sidebar.push({
        label,
        autogenerate: { directory: dir },
      });
    }
  }

  const banner =
    "// Auto-generated by skills/codebase-wiki/scripts/regenerate-starlight-sidebar.mjs\n" +
    "// Do not edit by hand; re-run the script after adding or renaming docs.\n\n";

  const body = `export const starlightSidebar = ${JSON.stringify(sidebar, null, 2)};\n`;

  fs.writeFileSync(outFile, banner + body, "utf8");
  console.log(`Wrote ${path.relative(root, outFile)}`);
}

main();
